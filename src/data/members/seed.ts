// Helper to build the seeded list with a consistent shape. Each entry is
// fully realized so the directory and profile pages have rich content
// without a lookup layer at render time.

function makeMember(input: Omit<Member, "slug" | "initials">): Member {
  const slug = input.name
    .toLowerCase()
    .normalize("NFD")
    .replace(/[̀-ͯ]/g, "")
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-");
  const initials = input.name
    .split(/\s+/)
    .filter(Boolean)
    .map((s) => s[0])
    .slice(0, 2)
    .join("")
    .toUpperCase();
  return { ...input, slug, initials };
}

