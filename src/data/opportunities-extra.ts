const ANCHOR_MS = new Date("2026-06-06T12:00:00.000Z").getTime();
const DAY_MS = 86_400_000;

function isoAt(daysAgo: number): string {
  return new Date(ANCHOR_MS - daysAgo * DAY_MS).toISOString().slice(0, 10);
}

function postedAgoFor(iso: string): string {
  const days = Math.max(
    0,
    Math.round((ANCHOR_MS - new Date(iso).getTime()) / DAY_MS)
  );
  if (days === 0) return "today";
  if (days === 1) return "1 day ago";
  if (days < 7) return `${days} days ago`;
  if (days < 14) return "1 week ago";
  if (days < 30) return `${Math.floor(days / 7)} weeks ago`;
  if (days < 60) return "1 month ago";
  return `${Math.floor(days / 30)} months ago`;
}

type NewOppConfig = {
  id: string; // "cc-007"
  publicId: string; // "OPP-000007" — image folder
  slug: string;
  title: string;
  category: string;
  industry: string;
  listingType?: ListingType;
  dealType: DealType;
  status: OpportunityStatus;
  place: Place;
  fundingAmount: number;
  fundingRange: string;
  expectedReturn: string;
  description: string;
  executiveSummary: string;
  fullDescription: string[];
  fundingRequired: string;
  equityAvailable: string;
  minimumInvestment: string;
  expectedReturns: string;
  currentStage: string;
  timeline: string;
  projectStatus: string;
  postedBy: string;
  daysAgo: number;
  companyId: string;
  imageCount: number;
  featured?: boolean;
  trending?: boolean;
  docs: { name: string; type: OpportunityDocument["type"]; pages: number; updated: string }[];
};

function makeOpp(c: NewOppConfig): Opportunity {
  const postedAt = isoAt(c.daysAgo);
  return {
    id: c.id,
    slug: c.slug,
    title: c.title,
    category: c.category,
    industry: c.industry,
    listingType: c.listingType ?? "Opportunity",
    dealType: c.dealType,
    location: `${c.place.city}${c.place.country ? `, ${c.place.country}` : ""}`,
    place: c.place,
    investmentRange: c.fundingRange,
    fundingAmount: c.fundingAmount,
    expectedReturn: c.expectedReturn,
    status: c.status,
    description: c.description,
    executiveSummary: c.executiveSummary,
    fullDescription: c.fullDescription,
    fundingRequired: c.fundingRequired,
    equityAvailable: c.equityAvailable,
    minimumInvestment: c.minimumInvestment,
    expectedReturns: c.expectedReturns,
    currentStage: c.currentStage,
    timeline: c.timeline,
    projectStatus: c.projectStatus,
    postedBy: c.postedBy,
    postedAgo: postedAgoFor(postedAt),
    postedAt,
    images: Array.from(
      { length: c.imageCount },
      (_, i) => `/opportunities/${c.publicId}/${i + 1}.svg`
    ),
    featured: !!c.featured,
    trending: !!c.trending,
    companyId: c.companyId,
    documents: c.docs,
  };
}

