import type {
  Company,
  CompanyPlace,
  GalleryCategory,
  PastProject,
  TeamRole,
  VerificationStatus,
} from "./types";

type NewCompanyConfig = {
  id: string;
  slug: string;
  name: string;
  tagline: string;
  industry: string;
  headquarters: CompanyPlace;
  website: string;
  foundedYear: number;
  employees: string;
  verification: VerificationStatus;
  about: Company["about"];
  team: Array<{ name: string; role: TeamRole; bio: string; yearsAtFirm: number }>;
  pastProjects: Array<Omit<PastProject, "id"> & { idSuffix: string }>;
  gallery: Array<{ category: GalleryCategory; caption: string }>;
  closedOpportunitiesCount: number;
  searchKeywords: string[];
  featured?: boolean;
  addedAt: string;
};

function initials(name: string): string {
  return name
    .split(/\s+/)
    .filter(Boolean)
    .map((s) => s[0])
    .slice(0, 2)
    .join("")
    .toUpperCase();
}

function makeCompany(c: NewCompanyConfig): Company {
  const websiteLabel = c.website.replace(/^https?:\/\//, "").replace(/\/$/, "");
  return {
    id: c.id,
    slug: c.slug,
    name: c.name,
    tagline: c.tagline,
    industry: c.industry,
    headquarters: c.headquarters,
    website: c.website,
    websiteLabel,
    foundedYear: c.foundedYear,
    employees: c.employees,
    verification: c.verification,
    coverImage: `/companies/${c.id}/cover.svg`,
    about: c.about,
    team: c.team.map((t) => ({
      name: t.name,
      role: t.role,
      bio: t.bio,
      initials: initials(t.name),
      yearsAtFirm: t.yearsAtFirm,
    })),
    pastProjects: c.pastProjects.map((p) => ({
      id: `PROJ-${c.id.split("-")[1]}-${p.idSuffix}`,
      name: p.name,
      location: p.location,
      year: p.year,
      description: p.description,
      category: p.category,
    })),
    gallery: c.gallery.map((g, i) => ({
      src: `/companies/${c.id}/gallery/${i + 1}.svg`,
      alt: `${g.category} — ${g.caption}`,
      category: g.category,
      caption: g.caption,
    })),
    closedOpportunitiesCount: c.closedOpportunitiesCount,
    searchKeywords: c.searchKeywords,
    featured: c.featured,
    addedAt: c.addedAt,
  };
}

