const ME = "Stevie Cabrera";

function act(
  i: number,
  kind: DealActivity["kind"],
  title: string,
  body: string,
  at: string
): DealActivity {
  return {
    id: `act-${i.toString().padStart(2, "0")}`,
    kind,
    title,
    body,
    createdAt: at,
    actor: ME,
  };
}

function note(i: number, text: string, at: string, name = ME): DealNote {
  return {
    id: `note-${i.toString().padStart(2, "0")}`,
    text,
    authorId: "me",
    authorName: name,
    createdAt: at,
  };
}

