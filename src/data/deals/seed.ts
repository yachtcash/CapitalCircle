import {
  DEAL_DESK_NOW_MS,
  statusForStage,
  type Deal,
  type DealActivity,
  type DealDocumentRef,
  type DealNote,
  type DealParticipant,
  type DealParty,
  type DealPriority,
  type DealSource,
  type DealStage,
} from "./types";

const DAY = 24 * 60 * 60 * 1000;
const iso = (daysAgo: number) => new Date(DEAL_DESK_NOW_MS - daysAgo * DAY).toISOString();
const isoAhead = (days: number) => new Date(DEAL_DESK_NOW_MS + days * DAY).toISOString();

type SeedConfig = {
  n: number; // numeric id
  title: string;
  stage: DealStage;
  priority?: DealPriority;
  sponsor: DealParty & { company?: string };
  investor?: DealParty & { company?: string };
  admin?: string;
  target: number;
  actual?: number;
  pct?: number;
  actualCommission?: number;
  createdDaysAgo: number;
  updatedDaysAgo?: number;
  expectedCloseInDays?: number;
  nextFollowUpInDays?: number;
  lastContactDaysAgo?: number;
  opportunitySlug?: string;
  opportunityId?: string;
  listingId?: string;
  companyId?: string;
  source?: DealSource;
  sourceId?: string;
  summary?: string;
  tags: string[];
  conversationIds?: string[];
  documents?: Array<Pick<DealDocumentRef, "name" | "type"> & { daysAgo?: number }>;
  notes?: Array<{ text: string; daysAgo: number }>;
  internalNotes?: Array<{ text: string; daysAgo: number }>;
  extraActivity?: Array<Pick<DealActivity, "kind" | "title" | "body"> & { daysAgo: number }>;
};

const OWNER = "Stevie Cabrera";

function mkDeal(c: SeedConfig): Deal {
  const dealId = `DEAL-${String(c.n).padStart(6, "0")}`;
  const pct = c.pct ?? 2.5;
  const created = iso(c.createdDaysAgo);
  const updated = iso(c.updatedDaysAgo ?? Math.max(0, c.createdDaysAgo - 2));
  const admin = c.admin ?? OWNER;

  const participants: DealParticipant[] = [
    {
      id: "p-sponsor",
      name: c.sponsor.name,
      company: c.sponsor.company ?? "—",
      role: "Sponsor",
      status: "Active",
      memberId: c.sponsor.memberId,
    },
  ];
  if (c.investor) {
    participants.push({
      id: "p-investor",
      name: c.investor.name,
      company: c.investor.company ?? "—",
      role: "Investor",
      status: "Active",
      memberId: c.investor.memberId,
    });
  }
  participants.push({
    id: "p-admin",
    name: admin,
    company: "Capital Circle",
    role: "Admin",
    status: "Active",
  });

  const activity: DealActivity[] = [
    {
      id: "act-01",
      kind: "created",
      title: "Deal created",
      body: `Opened by ${admin}.`,
      createdAt: created,
      actor: admin,
      actorRole: "Super Admin",
    },
    {
      id: "act-02",
      kind: "stage_change",
      title: `Stage → ${c.stage}`,
      createdAt: updated,
      actor: admin,
      actorRole: "Super Admin",
    },
    ...(c.extraActivity ?? []).map((a, i) => ({
      id: `act-${String(i + 3).padStart(2, "0")}`,
      kind: a.kind,
      title: a.title,
      body: a.body,
      createdAt: iso(a.daysAgo),
      actor: admin,
      actorRole: "Super Admin",
    })),
  ];

  const mkNotes = (src: SeedConfig["notes"]): DealNote[] =>
    (src ?? []).map((nn, i) => ({
      id: `note-${String(i + 1).padStart(2, "0")}`,
      text: nn.text,
      authorId: "me",
      authorName: admin,
      createdAt: iso(nn.daysAgo),
    }));

  return {
    dealId,
    title: c.title,
    opportunityId: c.opportunityId,
    opportunitySlug: c.opportunitySlug,
    listingId: c.listingId,
    companyId: c.companyId,
    sponsor: { name: c.sponsor.name, memberId: c.sponsor.memberId, companyId: c.sponsor.companyId },
    investor: c.investor
      ? { name: c.investor.name, memberId: c.investor.memberId, companyId: c.investor.companyId }
      : undefined,
    assignedAdmin: admin,
    stage: c.stage,
    status: statusForStage(c.stage),
    priority: c.priority ?? "Normal",
    createdDate: created,
    updatedDate: updated,
    expectedCloseDate:
      c.expectedCloseInDays !== undefined ? isoAhead(c.expectedCloseInDays) : undefined,
    targetInvestment: c.target,
    actualInvestment: c.actual,
    commissionPct: pct,
    estimatedCommission: Math.round((c.target * pct) / 100),
    actualCommission: c.actualCommission,
    sourceType: c.source ?? "Manual Entry",
    sourceId: c.sourceId,
    sourceName: c.sourceId,
    summaryNote: c.summary,
    notes: mkNotes(c.notes),
    internalNotes: mkNotes(c.internalNotes),
    activity,
    participants,
    documents: (c.documents ?? []).map((d, i) => ({
      id: `doc-${String(i + 1).padStart(2, "0")}`,
      name: d.name,
      type: d.type,
      addedAt: iso(d.daysAgo ?? c.createdDaysAgo),
      addedBy: admin,
    })),
    conversationIds: c.conversationIds ?? [],
    lastContactDate: c.lastContactDaysAgo !== undefined ? iso(c.lastContactDaysAgo) : undefined,
    nextFollowUpDate:
      c.nextFollowUpInDays !== undefined ? isoAhead(c.nextFollowUpInDays) : undefined,
    tags: c.tags,
  };
}

